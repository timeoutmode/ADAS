package com.example.adas.GuessingObjects;

public class FingerModel {
    String name;
    int image;


    public FingerModel(String name, int image) {
        this.name = name;
        this.image = image;

    }

    public String getName() {
        return name;
    }
    public int getImage() {
        return image;
    }


}
